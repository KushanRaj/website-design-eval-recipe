from __future__ import annotations

import argparse
import json
import shutil
import subprocess
import sys
import tomllib
from pathlib import Path
from typing import Any

from package_harbor_task import package_task


PROJECT_ROOT = Path(__file__).resolve().parents[1]


def _resolve_path(path: str | Path) -> Path:
    path = Path(path)
    if path.is_absolute():
        return path
    return PROJECT_ROOT / path


def _write_text(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text.strip() + "\n", encoding="utf-8")


def _display_path(path: Path) -> str:
    try:
        return str(path.relative_to(PROJECT_ROOT))
    except ValueError:
        return str(path)


def _write_dataset_toml(dataset_dir: Path, *, name: str, description: str) -> None:
    _write_text(
        dataset_dir / "dataset.toml",
        f"""
# Dataset manifest for {name}
# Generated by scripts/package_synthetic_dataset.py.

[dataset]
name = "{name}"
description = "{description}"
authors = []
keywords = ["synthetic", "website-replication", "screenshot-only", "visual-evaluation"]
""",
    )


def _write_readme(dataset_dir: Path, *, name: str) -> None:
    display_dir = _display_path(dataset_dir)
    _write_text(
        dataset_dir / "README.md",
        f"""
# {name}

Local Harbor task set generated from `Generator/output/harbor-dataset`.

Run locally with:

```bash
scripts/run_harbor_full_reward.sh {display_dir}
```

The agent-facing workspace contains only numbered reference screenshots. The
oracle site, replay manifest, and metric config are packaged under
`tests/private/` and are only available to the verifier.
""",
    )


def _discover_sites(source_root: Path) -> list[Path]:
    sites = [
        child / "site"
        for child in sorted(source_root.iterdir())
        if child.is_dir() and (child / "site" / "screenshot-manifest.json").exists()
    ]
    if not sites:
        raise FileNotFoundError(f"No generated sites found under {source_root}")
    return sites


def _run_harbor_add(dataset_dir: Path) -> None:
    harbor = shutil.which("harbor")
    if harbor is None:
        raise RuntimeError(
            "harbor CLI is required to write digest-pinned dataset.toml. "
            "Install/activate Harbor and rerun this script."
        )
    subprocess.run(
        [harbor, "add", str(dataset_dir), "--to", str(dataset_dir), "--scan"],
        cwd=PROJECT_ROOT,
        check=True,
    )


def _read_dataset_task_count(dataset_dir: Path) -> int:
    payload = tomllib.loads((dataset_dir / "dataset.toml").read_text(encoding="utf-8"))
    return len(payload.get("tasks") or [])


def package_dataset(
    *,
    source_root: Path,
    dataset_dir: Path,
    dataset_name: str,
    task_prefix: str,
    description: str,
    verifier_base_image: str,
    metric_profile: str,
    verifier_allow_internet: bool,
    force: bool,
) -> dict[str, Any]:
    source_root = source_root.resolve()
    dataset_dir = dataset_dir.resolve()
    if dataset_dir.exists():
        if not force:
            raise FileExistsError(f"Dataset directory exists; pass --force: {dataset_dir}")
        shutil.rmtree(dataset_dir)
    dataset_dir.mkdir(parents=True, exist_ok=True)

    sites = _discover_sites(source_root)
    org = dataset_name.split("/", 1)[0]
    packaged: list[dict[str, Any]] = []

    for site_dir in sites:
        slug = site_dir.parent.name
        task_name = f"{org}/{task_prefix}{slug}"
        task_dir = dataset_dir / slug
        package_task(
            site_dir,
            task_dir,
            task_name=task_name,
            force=True,
            verifier_base_image=verifier_base_image,
            metric_profile=metric_profile,
            vendor_evaluator=False,
            verifier_allow_internet=verifier_allow_internet,
        )
        packaged.append(
            {
                "site": slug,
                "task_name": task_name,
                "task_dir": _display_path(task_dir),
            }
        )

    _write_dataset_toml(dataset_dir, name=dataset_name, description=description)
    _write_readme(dataset_dir, name=dataset_name)
    _run_harbor_add(dataset_dir)

    task_count = _read_dataset_task_count(dataset_dir)
    if task_count != len(packaged):
        raise RuntimeError(
            f"dataset.toml contains {task_count} tasks, expected {len(packaged)}"
        )

    return {
        "dataset_dir": str(dataset_dir),
        "dataset_name": dataset_name,
        "task_count": len(packaged),
        "tasks": packaged,
    }


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(
        description="Package generated synthetic websites as a local Harbor dataset."
    )
    parser.add_argument("--source-root", default="Generator/output/harbor-dataset")
    parser.add_argument("--dataset-dir", default="datasets/synthetic-website-replication")
    parser.add_argument("--dataset-name", default="proximal/synthetic-website-replication")
    parser.add_argument(
        "--task-prefix",
        default="synthetic-website-replication-",
        help="Prefix applied after the org in each task name.",
    )
    parser.add_argument(
        "--description",
        default="Synthetic screenshot-only website replication dataset.",
    )
    parser.add_argument("--verifier-base-image", default="website-design-eval-verifier:latest")
    parser.add_argument(
        "--metric-profile",
        choices=["lite", "full-local", "full-vlm"],
        default="full-vlm",
    )
    parser.add_argument("--verifier-allow-internet", action="store_true")
    parser.add_argument("--force", action="store_true")
    args = parser.parse_args(argv)

    result = package_dataset(
        source_root=_resolve_path(args.source_root),
        dataset_dir=_resolve_path(args.dataset_dir),
        dataset_name=args.dataset_name,
        task_prefix=args.task_prefix,
        description=args.description,
        verifier_base_image=args.verifier_base_image,
        metric_profile=args.metric_profile,
        verifier_allow_internet=args.verifier_allow_internet,
        force=args.force,
    )
    print(json.dumps(result, indent=2, sort_keys=True))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
